#	include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t x;

	x = 0;
	if (!n)
		return (0);
	while (x < n && s1[x] && s2[x])
	{
		if ((s1)[x] != (s2)[x])
		{
			return ((s1)[x] - (s2)[x]);
		}
		x++;
	}
	if ((s1 || s2) && x < n)
		return ((s1)[x] - (s2)[x]);
	return (0);
}
