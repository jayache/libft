#	include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	size_t x;

	x = 0;
	if (!s1 && !s2)
		return (0);
	if ((!s1 && s2) || (s1 && !s2))
		return (s1[x] - s2[x]);
	while (s1[x] && s2[x])
	{
		if (s1[x] != s2[x])
		{
			return ((s1)[x] - (s2)[x]);
		}
		x++;
	}
	return ((s1)[x] - (s2)[x]);
}
